# Declare string variables
fname = "Ravindu"                       # Variable: fname (first name)
schl = "Ananda College"                 # Variable: schl (school)

# Display variables using print function
# Multiple arguments are separated by commas
print("Hello, I am", fname)             # Output: Hello, I am Ravindu
print("I am from ", schl)               # Output: I am from  Ananda College